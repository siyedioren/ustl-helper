/** 经纬度 → 图片百分比坐标 */
export const latLonToPercent = (lat: number, lon: number) => ({
  x: ((lon - MAP_BOUNDS.west) / (MAP_BOUNDS.east - MAP_BOUNDS.west)) * 100,
  y: ((MAP_BOUNDS.north - lat) / (MAP_BOUNDS.north - MAP_BOUNDS.south)) * 100,
});

